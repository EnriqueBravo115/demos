# Create spring maven project
curl https://start.spring.io/starter.zip -d groupId=com.polarbookshop 
-d artifactId=catalog-service -d name=catalog-service 
-d packageName=com.polarbookshop.catalogservice -d dependencies=web 
-d javaVersion=17 -d bootVersion=2.7.18 -d type=maven-project 
-o catalog-service.zip
